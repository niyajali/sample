package com.google.samples.apps.nowinandroid.core.designsystem.theme

import android.os.Build
import android.os.Build.VERSION_CODES
import androidx.annotation.ChecksSdkIntAtLeast
import androidx.compose.material3.ColorScheme
import androidx.compose.material3.dynamicDarkColorScheme
import androidx.compose.material3.dynamicLightColorScheme
import androidx.compose.runtime.Composable
import androidx.compose.ui.platform.LocalContext

@Composable
actual fun dynamicColorScheme(
    darkTheme: Boolean,
): ColorScheme {
    return when {
        Build.VERSION.SDK_INT >= VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        else -> if (darkTheme) DarkDefaultColorScheme else LightDefaultColorScheme
    }
}

@ChecksSdkIntAtLeast(api = VERSION_CODES.S)
actual fun supportsDynamicTheming(): Boolean = Build.VERSION.SDK_INT >= VERSION_CODES.S